def _target_missing_report_lines(rows: list[dict[str, Any]]) -> list[str]:
    if not rows:
        return ["- No missing target rows were found for reported vol-expansion targets."]
    lines = []
    for row in rows:
        lines.append(
            "- `{target}` missing reason `{reason}`: rows `{missing_rows}`, share `{share}`, "
            "roll `{roll}`, session open `{open_}`, session close `{close}`, warmup `{warmup}`, tail `{tail}`.".format(
                target=row.get("target"),
                reason=row.get("reason"),
                missing_rows=row.get("missing_rows"),
                share=_fmt(row.get("share_of_missing")),
                roll=row.get("roll_boundary_rows"),
                open_=row.get("session_open_rows"),
                close=row.get("session_close_rows"),
                warmup=row.get("warmup_rows"),
                tail=row.get("tail_end_rows"),
            )
        )
    return lines


def _assertion_report_lines(rows: list[dict[str, Any]]) -> list[str]:
    if not rows:
        return ["- No assertion checks were emitted."]
    return [
        f"- `{row.get('check_id')}`: `{row.get('status')}`; observed `{row.get('observed')}`; threshold `{row.get('threshold')}`; {row.get('message', '')}"
        for row in rows
    ]


def _smoke_report_lines(rows: list[dict[str, Any]]) -> list[str]:
    if not rows:
        return ["- Smoke/null diagnostics were not available."]
    return [
        f"- `{row.get('test')}`: n `{row.get('n')}`, ROC AUC `{_fmt(row.get('roc_auc'))}`, AUC edge `{_fmt(row.get('auc_edge_from_0_50'))}`, score/label corr `{_fmt(row.get('probability_label_corr'))}`."
        for row in rows
    ]


def _figure(figsize: tuple[float, float] = (8, 4.8)):
    import matplotlib.pyplot as plt

    fig, ax = plt.subplots(figsize=figsize)
    ax.grid(True, axis="y", alpha=0.25)
    return fig, ax


