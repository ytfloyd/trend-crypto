def _safe(value: Any) -> float | None:
    if value is None or pd.isna(value):
        return None
    return float(value)


def _numeric_col(frame: pd.DataFrame, col: str) -> pd.Series:
    if col not in frame:
        return pd.Series(np.nan, index=frame.index, dtype=float)
    return pd.to_numeric(frame[col], errors="coerce")


def _fmt(value: Any) -> str:
    if value is None or pd.isna(value):
        return "NA"
    return f"{float(value):.4f}"


def _row_by_value(frame: pd.DataFrame, col: str, value: str) -> dict[str, Any]:
    if frame.empty or col not in frame:
        return {}
    hit = frame[frame[col] == value]
    return hit.iloc[0].to_dict() if not hit.empty else {}
