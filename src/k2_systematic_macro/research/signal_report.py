def _target_coverage(panel: pd.DataFrame) -> pd.DataFrame:
    rows: list[dict[str, Any]] = []
    for col in sorted(c for c in panel.columns if c.startswith("target_future_vol_expansion_")):
        suffix = col.rsplit("_", 1)[-1]
        event_col = f"target_vol_expansion_event_{suffix}"
        valid = pd.to_numeric(panel[col], errors="coerce").replace([np.inf, -np.inf], np.nan).dropna()
        events = pd.to_numeric(panel[event_col], errors="coerce").dropna() if event_col in panel else pd.Series(dtype=float)
        rows.append(
            {
                "target": col,
                "valid_rows": int(valid.shape[0]),
                "coverage": float(valid.shape[0] / panel.shape[0]) if panel.shape[0] else np.nan,
                "median_ratio": _safe(valid.median()),
                "event_rate": _safe(events.mean()),
            }
        )
    return pd.DataFrame(rows)


def _target_missing_reason_rows(
    panel: pd.DataFrame,
    dataset: ResearchDataset | None = None,
    primary_timeframe: str = "4h",
) -> pd.DataFrame:
    """Classify missing forward-target rows into deterministic, known buckets."""

    columns = sorted(c for c in panel.columns if c.startswith("target_future_vol_expansion_"))
    if panel.empty or not columns:
        return pd.DataFrame(
            columns=[
                "target",
                "ts",
                "row_index",
                "reason",
                "is_roll_boundary",
                "is_session_open",
                "is_session_close",
                "is_warmup",
                "is_tail_end_of_sample",
            ]
        )
    work = panel.sort_values("ts").reset_index(drop=True).copy()
    ts = pd.to_datetime(work["ts"], utc=True, errors="coerce") if "ts" in work else pd.Series(pd.NaT, index=work.index)
    roll_window = _roll_boundary_mask(work, dataset)
    session_open, session_close = _session_edge_masks(work)
    rows: list[dict[str, Any]] = []
    n_rows = int(work.shape[0])
    for target in columns:
        suffix = target.rsplit("_", 1)[-1]
        values = pd.to_numeric(work[target], errors="coerce").replace([np.inf, -np.inf], np.nan)
        missing = values.isna()
        if not missing.any():
            continue
        valid_positions = np.flatnonzero(values.notna().to_numpy())
        first_valid = int(valid_positions[0]) if len(valid_positions) else n_rows
        horizon_bars = _target_horizon_bars(primary_timeframe, suffix)
        tail_start = max(n_rows - horizon_bars, 0) if horizon_bars is not None else n_rows
        for idx in np.flatnonzero(missing.to_numpy()):
            is_tail = bool(idx >= tail_start)
            is_warmup = bool(idx < first_valid)
            is_roll = bool(roll_window.iloc[idx]) if idx < roll_window.shape[0] else False
            is_open = bool(session_open.iloc[idx]) if idx < session_open.shape[0] else False
            is_close = bool(session_close.iloc[idx]) if idx < session_close.shape[0] else False
            rows.append(
                {
                    "target": target,
                    "ts": ts.iloc[idx].isoformat() if not pd.isna(ts.iloc[idx]) else None,
                    "row_index": int(idx),
                    "reason": _target_missing_reason(is_tail, is_warmup, is_roll, is_open, is_close),
                    "is_roll_boundary": is_roll,
                    "is_session_open": is_open,
                    "is_session_close": is_close,
                    "is_warmup": is_warmup,
                    "is_tail_end_of_sample": is_tail,
                }
            )
    return pd.DataFrame(rows)


def _target_missing_reason_summary(missing_rows: pd.DataFrame) -> pd.DataFrame:
    if missing_rows.empty:
        return pd.DataFrame(
            columns=[
                "target",
                "reason",
                "missing_rows",
                "share_of_missing",
                "roll_boundary_rows",
                "session_open_rows",
                "session_close_rows",
                "warmup_rows",
                "tail_end_rows",
            ]
        )
    totals = missing_rows.groupby("target").size().rename("total_missing")
    summary = (
        missing_rows.groupby(["target", "reason"], dropna=False)
        .agg(
            missing_rows=("reason", "size"),
            roll_boundary_rows=("is_roll_boundary", "sum"),
            session_open_rows=("is_session_open", "sum"),
            session_close_rows=("is_session_close", "sum"),
            warmup_rows=("is_warmup", "sum"),
            tail_end_rows=("is_tail_end_of_sample", "sum"),
        )
        .reset_index()
    )
    summary = summary.merge(totals.reset_index(), on="target", how="left")
    summary["share_of_missing"] = summary["missing_rows"] / summary["total_missing"].replace(0, np.nan)
    return summary.drop(columns=["total_missing"]).sort_values(["target", "missing_rows"], ascending=[True, False])


def _target_missing_reason(
    is_tail: bool,
    is_warmup: bool,
    is_roll: bool,
    is_open: bool,
    is_close: bool,
) -> str:
    if is_tail:
        return "tail_end_of_sample"
    if is_warmup:
        return "warmup"
    if is_roll:
        return "roll_boundary"
    if is_open:
        return "session_open"
    if is_close:
        return "session_close"
    return "other"


def _roll_boundary_mask(panel: pd.DataFrame, dataset: ResearchDataset | None) -> pd.Series:
    mask = pd.Series(False, index=panel.index)
    if "expiry" in panel:
        expiry = panel["expiry"].astype(str)
        changed = expiry.ne(expiry.shift(1)) | expiry.ne(expiry.shift(-1))
        mask = mask | changed.fillna(False)
    schedule = (dataset.metadata.get("roll_schedule") if dataset is not None else None) or []
    if "ts" not in panel or not schedule:
        return mask
    ts = pd.to_datetime(panel["ts"], utc=True, errors="coerce")
    for row in schedule:
        roll_ts = pd.to_datetime(row.get("roll_date"), utc=True, errors="coerce")
        if pd.isna(roll_ts):
            continue
        near = (ts.dt.normalize() - roll_ts.normalize()).dt.days.abs().le(1)
        mask = mask | near.fillna(False)
    return mask


def _session_edge_masks(panel: pd.DataFrame) -> tuple[pd.Series, pd.Series]:
    if "session_date" not in panel:
        empty = pd.Series(False, index=panel.index)
        return empty, empty
    grouped = panel.groupby("session_date", dropna=False).cumcount()
    reverse = panel.iloc[::-1].groupby("session_date", dropna=False).cumcount().iloc[::-1]
    return grouped.eq(0), reverse.eq(0)


def _target_horizon_bars(primary_timeframe: str, suffix: str) -> int | None:
    mapping = {
        "1h": {"4h": 4, "1d": 24, "3d": 72, "5d": 120},
        "4h": {"4h": 1, "1d": 6, "3d": 18, "5d": 30},
        "1d": {"1d": 1, "3d": 3, "5d": 5},
        "daily": {"1d": 1, "3d": 3, "5d": 5},
    }
    return mapping.get(primary_timeframe.lower(), {}).get(suffix.lower())


def _roll_sensitivity(config: CLResearchConfig) -> tuple[pd.DataFrame, pd.DataFrame]:
    if config.source.lower() not in {"auto", "duckdb"} or not config.lake_path.exists():
        empty = pd.DataFrame()
        return empty, empty
    bars = _load_dated_bars(config)
    if bars.empty:
        empty = pd.DataFrame()
        return empty, empty
    policy = RollPolicy(
        name=config.continuous_roll_policy,  # type: ignore[arg-type]
        roll_window_business_days=config.continuous_roll_window_business_days,
        forced_roll_business_days_before_last_trade=config.continuous_forced_roll_business_days_before_last_trade,
        volume_crossover_sessions=config.continuous_volume_crossover_sessions,
    )
    guard = None
    weekend = construct_continuous_series(
        bars,
        symbol=config.symbol,
        policy=policy,
        adjustment=config.continuous_adjustment,  # type: ignore[arg-type]
        calendar=WeekendHolidayCalendar(),
        front_month_guard=guard,
    )
    nymex = construct_continuous_series(
        bars,
        symbol=config.symbol,
        policy=policy,
        adjustment=config.continuous_adjustment,  # type: ignore[arg-type]
        calendar=NymexObservedHolidayCalendar(),
        front_month_guard=guard,
    )
    if weekend.schedule.empty or nymex.schedule.empty:
        summary = pd.DataFrame(
            [
                {
                    "weekend_panel_rows": int(weekend.bars.shape[0]),
                    "nymex_panel_rows": int(nymex.bars.shape[0]),
                    "roll_events": 0,
                    "roll_date_changes": 0,
                    "last_trade_date_changes": 0,
                    "roll_gap_changes": 0,
                    "active_expiry_sequence_changed": False,
                    "panel_rows_changed": int(weekend.bars.shape[0]) != int(nymex.bars.shape[0]),
                }
            ]
        )
        return summary, pd.DataFrame()
    compare = weekend.schedule.add_prefix("weekend_").merge(
        nymex.schedule.add_prefix("nymex_"),
        left_on=["weekend_front_expiry", "weekend_next_expiry"],
        right_on=["nymex_front_expiry", "nymex_next_expiry"],
        how="outer",
    )
    if compare.empty:
        return pd.DataFrame(), compare
    compare["roll_date_changed"] = compare["weekend_roll_date"] != compare["nymex_roll_date"]
    compare["last_trade_date_changed"] = compare["weekend_last_trade_date"] != compare["nymex_last_trade_date"]
    compare["roll_gap_changed"] = ~np.isclose(
        pd.to_numeric(compare["weekend_roll_gap"], errors="coerce"),
        pd.to_numeric(compare["nymex_roll_gap"], errors="coerce"),
        equal_nan=True,
    )
    compare["panel_rows_changed"] = int(weekend.bars.shape[0]) != int(nymex.bars.shape[0])
    compare["active_expiry_sequence_changed"] = not weekend.bars["expiry"].reset_index(drop=True).equals(
        nymex.bars["expiry"].reset_index(drop=True)
    )
    summary = pd.DataFrame(
        [
            {
                "weekend_panel_rows": int(weekend.bars.shape[0]),
                "nymex_panel_rows": int(nymex.bars.shape[0]),
                "roll_events": int(compare.shape[0]),
                "roll_date_changes": int(compare["roll_date_changed"].sum()),
                "last_trade_date_changes": int(compare["last_trade_date_changed"].sum()),
                "roll_gap_changes": int(compare["roll_gap_changed"].sum()),
                "active_expiry_sequence_changed": bool(compare["active_expiry_sequence_changed"].any()),
                "panel_rows_changed": bool(compare["panel_rows_changed"].any()),
            }
        ]
    )
    return summary, compare


def _roll_sensitivity_assertions(summary: pd.DataFrame) -> pd.DataFrame:
    rows = [
        {
            "check_id": "roll_sensitivity_available",
            "status": "PASS" if not summary.empty else "WARN",
            "observed": bool(not summary.empty),
            "threshold": "summary must be non-empty",
            "message": "Roll sensitivity comparison was generated."
            if not summary.empty
            else "Roll sensitivity comparison is unavailable for this source/config.",
        }
    ]
    if not summary.empty:
        row = summary.iloc[0]
        sequence_changed_same_rows = bool(row.get("active_expiry_sequence_changed")) and not bool(
            row.get("panel_rows_changed")
        )
        rows.append(
            {
                "check_id": "roll_sequence_changed_same_rows",
                "status": "WARN" if sequence_changed_same_rows else "PASS",
                "observed": sequence_changed_same_rows,
                "threshold": "warn when active expiry sequence changes while row count is unchanged",
                "message": (
                    "Active-expiry sequence changed while panel row count did not; inspect calendar/roll assumptions."
                    if sequence_changed_same_rows
                    else "No internal consistency warning from roll sequence vs row-count comparison."
                ),
            }
        )
    return pd.DataFrame(rows)


def _load_dated_bars(config: CLResearchConfig) -> pd.DataFrame:
    with MinuteLake(config.lake_path) as lake:
        clauses = ["symbol = ?", "expiry != ?"]
        params: list[Any] = [config.symbol.upper(), CONTINUOUS_EXPIRY]
        if config.start_ts is not None:
            clauses.append("ts >= ?")
            params.append(pd.Timestamp(config.start_ts).to_pydatetime())
        if config.end_ts is not None:
            clauses.append("ts < ?")
            params.append(pd.Timestamp(config.end_ts).to_pydatetime())
        rows = lake.connect().execute(
            f"""
            SELECT ts, symbol, expiry, o, h, l, c, v
            FROM {lake.bars_table}
            WHERE {" AND ".join(clauses)}
            ORDER BY ts, expiry
            """,
            params,
        ).fetchall()
    return pd.DataFrame(rows, columns=["ts", "symbol", "expiry", "o", "h", "l", "c", "v"])


def _preferred_model(results: dict[str, ExpansionModelResult]) -> tuple[str, ExpansionModelResult | None]:
    for name in ("lightgbm", "logistic", "xgboost"):
        result = results.get(name)
        if result is not None and not result.predictions.empty:
            return name, result
    for name, result in results.items():
        if not result.predictions.empty:
            return name, result
    return "none", None


def _prediction_panel(panel: pd.DataFrame, predictions: pd.DataFrame) -> pd.DataFrame:
    if predictions.empty:
        return pd.DataFrame()
    keep = [
        "ts",
        "regime",
        "target_future_log_return_1d",
        "target_future_return_1d",
        "target_future_move_norm_1d",
        "target_future_vol_expansion_1d",
        "target_vol_expansion_event_1d",
        "target_tail_event_1d",
    ]
    extra = [col for col in keep if col in panel]
    out = predictions.merge(panel[extra], on="ts", how="left", suffixes=("", "_panel"))
    out["regime"] = pd.to_numeric(out["regime"], errors="coerce")
    out["decile"] = pd.qcut(out["y_prob"].rank(method="first"), q=10, labels=False, duplicates="drop")
    return out


def _regime_decile_diagnostics(joined: pd.DataFrame) -> pd.DataFrame:
    if joined.empty or "decile" not in joined:
        return pd.DataFrame()
    rows = []
    for (regime, decile), group in joined.dropna(subset=["regime", "decile"]).groupby(["regime", "decile"]):
        fwd = _numeric_col(group, "target_future_log_return_1d")
        event_rate = float(group["y_true"].mean())
        mean_probability = float(group["y_prob"].mean())
        tail_participation = _safe(_numeric_col(group, "target_tail_event_1d").mean())
        drawdown_proxy = _safe(_max_drawdown(fwd.dropna()))
        rows.append(
            {
                "regime": int(regime),
                "probability_decile": int(decile) + 1,
                "n": int(group.shape[0]),
                "mean_probability": mean_probability,
                "event_rate": event_rate,
                "tail_participation": tail_participation,
                "mean_forward_log_return_1d": _safe(fwd.mean()),
                "skew_forward_log_return_1d": _safe(fwd.skew()),
                "drawdown_proxy": drawdown_proxy,
                "mean_future_move_norm_1d": _safe(
                    _numeric_col(group, "target_future_move_norm_1d").mean()
                ),
                "convexity_research_score": _convexity_research_score(
                    event_rate=event_rate,
                    mean_probability=mean_probability,
                    tail_participation=tail_participation,
                    drawdown_proxy=drawdown_proxy,
                ),
            }
        )
    return pd.DataFrame(rows).sort_values(["regime", "probability_decile"]).reset_index(drop=True)


def _convexity_research_score(
    *,
    event_rate: float | None,
    mean_probability: float | None,
    tail_participation: float | None,
    drawdown_proxy: float | None,
) -> float | None:
    """Return the reproducible convexity research score used in report ranking.

    Formula:
    ``event_rate + mean_probability + tail_participation - abs(drawdown_proxy)``.
    Null ``tail_participation`` and ``drawdown_proxy`` are treated as zero so the
    score can be recomputed from the output columns for every selected cell.
    """

    if event_rate is None or mean_probability is None or pd.isna(event_rate) or pd.isna(mean_probability):
        return None
    tail = 0.0 if tail_participation is None or pd.isna(tail_participation) else float(tail_participation)
    drawdown = 0.0 if drawdown_proxy is None or pd.isna(drawdown_proxy) else abs(float(drawdown_proxy))
    return float(event_rate) + float(mean_probability) + tail - drawdown


def _select_top_cells(
    regime_deciles: pd.DataFrame,
    config: TopCellSelectorConfig | None = None,
    max_cells: int = 5,
) -> pd.DataFrame:
    """Select top regime/decile cells with explicit min-n, sort key, and tie-breakers."""

    cfg = config or TopCellSelectorConfig()
    if regime_deciles.empty:
        return pd.DataFrame()
    missing_sort_cols = [col for col in cfg.sort_key if col not in regime_deciles]
    if missing_sort_cols:
        raise ValueError(f"top-cell selector missing sort columns: {missing_sort_cols}")
    work = regime_deciles.copy()
    work = work[pd.to_numeric(work["n"], errors="coerce") >= cfg.min_n].copy()
    if work.empty:
        return pd.DataFrame(columns=[*regime_deciles.columns, "selector_rank"])
    work = work.sort_values(list(cfg.sort_key), ascending=list(cfg.ascending), kind="mergesort")
    out = work.head(max_cells).reset_index(drop=True)
    out.insert(0, "selector_rank", np.arange(1, out.shape[0] + 1))
    return out


def _fold_diagnostics(predictions: pd.DataFrame, bins: int = 5) -> pd.DataFrame:
    if predictions.empty:
        return pd.DataFrame()
    rows = []
    for fold, group in predictions.groupby("fold"):
        y = group["y_true"].astype(float)
        auc = _roc_auc(y, group["y_prob"].astype(float))
        calibration = _fold_calibration(group, bins)
        event_rates = calibration["event_rate"].astype(float).to_list() if not calibration.empty else []
        spearman = _spearman(calibration["bin"], calibration["event_rate"]) if not calibration.empty else None
        monotonic = bool(calibration["event_rate"].is_monotonic_increasing) if not calibration.empty else False
        rows.append(
            {
                "fold": int(fold),
                "n": int(group.shape[0]),
                "events": int(y.sum()),
                "event_rate": float(y.mean()),
                "mean_probability": float(group["y_prob"].mean()),
                "roc_auc": auc,
                "auc_below_0_50": bool(auc is not None and auc < 0.50),
                "calibration_bins": int(calibration.shape[0]),
                "calibration_event_rate_vector": json.dumps(event_rates),
                "calibration_spearman": spearman,
                "calibration_spearman_positive": bool(spearman is not None and spearman > 0.0),
                "calibration_monotonic": monotonic,
            }
        )
    return pd.DataFrame(rows)


def _fold_calibration(group: pd.DataFrame, bins: int) -> pd.DataFrame:
    work = group.copy()
    if work.empty:
        return pd.DataFrame()
    work["bin"] = pd.qcut(work["y_prob"].rank(method="first"), q=bins, labels=False, duplicates="drop")
    return work.groupby("bin").agg(event_rate=("y_true", "mean"), n=("y_true", "size")).reset_index()


