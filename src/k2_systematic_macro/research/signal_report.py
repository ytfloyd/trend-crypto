def _spearman(x: pd.Series, y: pd.Series) -> float | None:
    work = pd.DataFrame({"x": x, "y": y}).replace([np.inf, -np.inf], np.nan).dropna()
    if work.shape[0] < 2 or work["x"].nunique() < 2 or work["y"].nunique() < 2:
        return None
    xr = work["x"].rank(method="average")
    yr = work["y"].rank(method="average")
    corr = xr.corr(yr)
    return None if pd.isna(corr) else float(corr)


def _roc_auc(y_true: pd.Series, y_prob: pd.Series) -> float | None:
    if y_true.nunique() < 2:
        return None
    from sklearn.metrics import roc_auc_score

    return float(roc_auc_score(y_true, y_prob))


def _regime2_overlay(joined: pd.DataFrame) -> pd.DataFrame:
    if joined.empty or "regime" not in joined:
        return pd.DataFrame()
    variants = {
        "baseline_all_regimes": np.ones(joined.shape[0]),
        "exclude_regime_2": np.where(joined["regime"] == 2, 0.0, 1.0),
        "regime_2_risk_off_no_entry": np.where(joined["regime"] == 2, 0.0, 1.0),
        "regime_2_vol_size_down_50pct": np.where(joined["regime"] == 2, 0.5, 1.0),
    }
    rows = []
    total_events = float(joined["y_true"].sum())
    for name, weights in variants.items():
        w = pd.Series(weights, index=joined.index, dtype=float)
        effective_n = float(w.sum())
        event_weight = float((joined["y_true"] * w).sum())
        fwd = _numeric_col(joined, "target_future_log_return_1d").fillna(0.0) * w
        tails = _numeric_col(joined, "target_tail_event_1d").fillna(0.0)
        rows.append(
            {
                "variant": name,
                "effective_sample": effective_n,
                "sample_loss": float(1.0 - effective_n / joined.shape[0]) if joined.shape[0] else np.nan,
                "event_capture": float(event_weight / total_events) if total_events else np.nan,
                "weighted_event_rate": float(event_weight / effective_n) if effective_n else np.nan,
                "weighted_tail_participation": float((tails * w).sum() / effective_n) if effective_n else np.nan,
                "drawdown_proxy": _safe(_max_drawdown(fwd)),
                "regime2_rows_removed_or_downweighted": int((joined["regime"] == 2).sum()),
            }
        )
    return pd.DataFrame(rows)


def _smoke_null_diagnostics(predictions: pd.DataFrame, *, seed: int) -> pd.DataFrame:
    """Run deterministic null-label diagnostics against existing prediction scores."""

    if predictions.empty:
        return pd.DataFrame(
            [
                {
                    "test": "shuffled_labels",
                    "seed": seed,
                    "n": 0,
                    "roc_auc": None,
                    "auc_edge_from_0_50": None,
                    "probability_label_corr": None,
                    "event_rate": None,
                }
            ]
        )
    rng = np.random.default_rng(seed)
    y_prob = pd.to_numeric(predictions["y_prob"], errors="coerce")
    y_true = pd.to_numeric(predictions["y_true"], errors="coerce")
    valid = pd.DataFrame({"y_prob": y_prob, "y_true": y_true}).dropna()
    shuffled = valid["y_true"].to_numpy().copy()
    rng.shuffle(shuffled)
    shuffled_series = pd.Series(shuffled, index=valid.index, dtype=float)
    auc = _roc_auc(shuffled_series, valid["y_prob"].astype(float))
    corr = valid["y_prob"].astype(float).corr(shuffled_series)
    null_prob = pd.Series(float(valid["y_true"].mean()), index=valid.index)
    null_auc = _roc_auc(valid["y_true"].astype(float), null_prob)
    return pd.DataFrame(
        [
            {
                "test": "shuffled_labels",
                "seed": seed,
                "n": int(valid.shape[0]),
                "roc_auc": auc,
                "auc_edge_from_0_50": None if auc is None else float(auc - 0.50),
                "probability_label_corr": None if pd.isna(corr) else float(corr),
                "event_rate": float(shuffled_series.mean()) if not shuffled_series.empty else None,
            },
            {
                "test": "constant_base_rate_score",
                "seed": seed,
                "n": int(valid.shape[0]),
                "roc_auc": null_auc,
                "auc_edge_from_0_50": None if null_auc is None else float(null_auc - 0.50),
                "probability_label_corr": 0.0,
                "event_rate": float(valid["y_true"].mean()) if not valid.empty else None,
            },
        ]
    )


def _diagnostics_harness(
    *,
    target_missing_reasons: pd.DataFrame,
    roll_assertions: pd.DataFrame,
    fold_diagnostics: pd.DataFrame,
    calibration_summary: pd.DataFrame,
    selected_cells: pd.DataFrame,
    smoke_null: pd.DataFrame,
    thresholds: DiagnosticsThresholdConfig,
) -> pd.DataFrame:
    rows: list[dict[str, Any]] = []
    if target_missing_reasons.empty:
        rows.append(_check("target_missing_reason_classification", "PASS", 0, "all missing targets classified"))
    else:
        known = target_missing_reasons[target_missing_reasons["reason"] != "other"]["missing_rows"].sum()
        total = target_missing_reasons["missing_rows"].sum()
        rows.append(
            _check(
                "target_missing_reason_classification",
                "PASS" if known == total else "WARN",
                f"{known}/{total}",
                "all missing target rows should map to known reasons",
                "classification emitted",
            )
        )
    for row in roll_assertions.to_dict(orient="records"):
        rows.append(_check(str(row["check_id"]), str(row["status"]), row.get("observed"), row.get("threshold"), row.get("message")))
    cal = calibration_summary.iloc[0].to_dict() if not calibration_summary.empty else {}
    share_monotonic = cal.get("share_monotonic")
    rows.append(
        _threshold_check(
            "calibration_monotonic_share",
            share_monotonic,
            thresholds.calibration_monotonic_share_min,
            warn_only=True,
            comparator=">=",
        )
    )
    rows.append(
        _threshold_check(
            "calibration_median_spearman",
            cal.get("median_spearman"),
            thresholds.calibration_spearman_median_min,
            warn_only=True,
            comparator=">=",
        )
    )
    top_n = int(pd.to_numeric(selected_cells["n"], errors="coerce").min()) if not selected_cells.empty else 0
    rows.append(
        _threshold_check(
            "top_cell_min_n",
            top_n,
            thresholds.top_cell_min_n,
            warn_only=True,
            comparator=">=",
        )
    )
    shuffled = _row_by_value(smoke_null, "test", "shuffled_labels")
    rows.append(
        _threshold_check(
            "smoke_shuffled_auc_edge_abs",
            abs(float(shuffled.get("auc_edge_from_0_50"))) if shuffled.get("auc_edge_from_0_50") is not None else None,
            thresholds.smoke_auc_edge_max_abs,
            warn_only=False,
            comparator="<=",
        )
    )
    rows.append(
        _threshold_check(
            "smoke_shuffled_prob_label_corr_abs",
            abs(float(shuffled.get("probability_label_corr"))) if shuffled.get("probability_label_corr") is not None else None,
            thresholds.smoke_probability_label_corr_max_abs,
            warn_only=False,
            comparator="<=",
        )
    )
    return pd.DataFrame(rows)


def _check(
    check_id: str,
    status: str,
    observed: Any,
    threshold: Any,
    message: str | None = None,
) -> dict[str, Any]:
    return {
        "check_id": check_id,
        "status": status.upper(),
        "observed": observed,
        "threshold": threshold,
        "message": message or "",
    }


def _threshold_check(
    check_id: str,
    observed: Any,
    threshold: float | int,
    *,
    warn_only: bool,
    comparator: str,
) -> dict[str, Any]:
    if observed is None or pd.isna(observed):
        return _check(check_id, "WARN" if warn_only else "FAIL", observed, f"{comparator} {threshold}", "metric unavailable")
    value = float(observed)
    passed = value >= float(threshold) if comparator == ">=" else value <= float(threshold)
    if passed:
        return _check(check_id, "PASS", value, f"{comparator} {threshold}", "threshold satisfied")
    return _check(
        check_id,
        "WARN" if warn_only else "FAIL",
        value,
        f"{comparator} {threshold}",
        "threshold not satisfied",
    )


