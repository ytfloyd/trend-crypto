def _git_manifest() -> dict[str, Any]:
    root = Path(__file__).resolve().parents[3]
    try:
        sha = subprocess.check_output(["git", "rev-parse", "HEAD"], cwd=root, text=True).strip()
        status = subprocess.check_output(["git", "status", "--porcelain"], cwd=root, text=True)
        return {
            "sha": sha,
            "dirty": bool(status.strip()),
            "dirty_entries": len([line for line in status.splitlines() if line.strip()]),
        }
    except Exception as exc:  # pragma: no cover - depends on local git availability
        return {"sha": None, "dirty": None, "unavailable_reason": str(exc)}


def _package_versions(names: list[str]) -> dict[str, str | None]:
    versions: dict[str, str | None] = {}
    for name in names:
        try:
            versions[name] = importlib.metadata.version(name)
        except importlib.metadata.PackageNotFoundError:
            versions[name] = None
    return versions


def _dataset_hash(panel: pd.DataFrame) -> dict[str, Any]:
    relevant = [
        col
        for col in (
            "ts",
            "expiry",
            "c",
            "regime",
            "regime_probability",
            "target_future_vol_expansion_4h",
            "target_future_vol_expansion_1d",
            "target_vol_expansion_event_1d",
        )
        if col in panel
    ]
    work = panel[relevant].copy() if relevant else panel.copy()
    for col in work.columns:
        if pd.api.types.is_datetime64_any_dtype(work[col]):
            work[col] = pd.to_datetime(work[col], utc=True).astype("int64")
    hashed = pd.util.hash_pandas_object(work.reset_index(drop=True), index=False).to_numpy(dtype="uint64")
    digest = hashlib.sha256(hashed.tobytes()).hexdigest()
    digest = hashlib.sha256((digest + json.dumps({"columns": relevant, "rows": int(work.shape[0])})).encode()).hexdigest()
    return {"algorithm": "sha256_pandas_hash", "columns": relevant, "rows": int(work.shape[0]), "sha256": digest}


def _jsonable(value: Any) -> Any:
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, dict):
        return {str(k): _jsonable(v) for k, v in value.items()}
    if isinstance(value, (list, tuple)):
        return [_jsonable(v) for v in value]
    return value


def _max_drawdown(returns: pd.Series) -> float:
    if returns.empty:
        return np.nan
    equity = np.exp(returns.cumsum())
    return float((equity / equity.cummax() - 1.0).min())


