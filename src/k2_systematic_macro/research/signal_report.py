CONVEXITY_RESEARCH_SCORE_DEFINITION = (
    "convexity_research_score = event_rate + mean_probability + tail_participation "
    "- abs(drawdown_proxy), where null tail_participation and drawdown_proxy are treated as 0. "
    "It is a reproducible diagnostic ranking score only, not an expected-return or trading score."
)


@dataclass(frozen=True)
class SignalResearchArtifacts:
    """Files generated by the signal-research report layer."""

    paths: dict[str, Path]
    summary: dict[str, Any]


@dataclass(frozen=True)
class TopCellSelectorConfig:
    """Deterministic selector for highlighted regime/probability-decile cells."""

    min_n: int = 30
    sort_key: tuple[str, ...] = (
        "event_rate",
        "mean_probability",
        "convexity_research_score",
        "n",
        "regime",
        "probability_decile",
    )
    ascending: tuple[bool, ...] = (False, False, False, False, True, False)


@dataclass(frozen=True)
class DiagnosticsThresholdConfig:
    """Assertion-style diagnostic thresholds for report health, not strategy quality."""

    calibration_monotonic_share_min: float = 0.50
    calibration_spearman_median_min: float = 0.0
    top_cell_min_n: int = 30
    smoke_auc_edge_max_abs: float = 0.10
    smoke_probability_label_corr_max_abs: float = 0.10
    smoke_seed: int = 42


