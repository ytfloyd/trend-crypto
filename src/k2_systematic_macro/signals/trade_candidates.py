def _convexity_score(
    event_rate: float | None,
    mean_probability: float | None,
    tail_participation: float | None,
    drawdown_proxy: float | None,
) -> float | None:
    if event_rate is None or mean_probability is None:
        return None
    tail = 0.0 if tail_participation is None or pd.isna(tail_participation) else float(tail_participation)
    drawdown = 0.0 if drawdown_proxy is None or pd.isna(drawdown_proxy) else abs(float(drawdown_proxy))
    return float(event_rate) + float(mean_probability) + tail - drawdown


def _confidence_score(
    expansion_prob: float | None,
    directional_confidence: float | None,
    directional_asymmetry: float | None,
    config: TradeCandidateConfig,
) -> float | None:
    if expansion_prob is None or directional_confidence is None or directional_asymmetry is None:
        return None
    threshold = max(config.min_directional_asymmetry, 1e-9)
    asymmetry_scale = min(1.0, abs(directional_asymmetry) / threshold)
    return float(expansion_prob * directional_confidence * asymmetry_scale)


def _max_drawdown(returns: pd.Series) -> float:
    if returns.empty:
        return np.nan
    equity = np.exp(returns.cumsum())
    return float((equity / equity.cummax() - 1.0).min())


def _numeric_col(frame: pd.DataFrame, col: str) -> pd.Series:
    if col not in frame:
        return pd.Series(np.nan, index=frame.index, dtype=float)
    return pd.to_numeric(frame[col], errors="coerce")


def _max_or_none(left: float | None, right: float | None) -> float | None:
    values = [value for value in [left, right] if value is not None and not pd.isna(value)]
    return max(values) if values else None


